export const products: Product[] = seeds.map((s, i) => {
  const slug = slugify(s.name);
  const { imgA, imgB, ...rest } = s;
  return {
    ...rest,
    id: `prod-${String(i + 1).padStart(3, "0")}`,
    slug,
    images: pair(imgA, imgB, s.name),
    tryOnImage: u(POOL[imgA % POOL.length], 600),
    specs: {
      Marca: s.brand,
      Formato: s.shape,
      Material: s.material,
      Tamanho: s.size,
      "Proteção UV": s.uvProtection ? "UV400" : "Não aplicável",
      Gênero: s.gender,
      Garantia: "12 meses",
      Lentes: s.uvProtection ? "Polarizadas / Categoria 3" : "Antirreflexo",
    },
  };
});

export const getProductBySlug = (slug: string) =>
  products.find((p) => p.slug === slug);

export const bestSellers = products.filter((p) => p.isBestSeller);
export const newArrivals = products.filter((p) => p.isNew);

export const relatedProducts = (product: Product, limit = 4) =>
  products
    .filter(
      (p) =>
        p.id !== product.id &&
        p.categories.some((c) => product.categories.includes(c)),
    )
    .slice(0, limit);
